window.addEventListener('load', async () => {
  // Wait for loading completion to avoid race conditions with web3 injection timing.
  if (window.ethereum) {
    const web3 = new Web3(window.ethereum);
    try {
      // Request account access if needed
      await window.ethereum.enable();
      // Accounts now exposed
      window.web3 = web3;
      return App.init(window.ethereum);
    } catch (error) {
      console.error(error);
    }
  }
  // Legacy dapp browsers...
  else if (window.web3) {
    // Use MetaMask/Mist's provider.
    const web3 = window.web3;
    console.log('Injected web3 detected.');
    return App.init(window.web3.currentProvider);
  }
  // Fallback to preset url; use dev console port by default...
  else {
    const provider = new Web3.providers.HttpProvider('http://212.66.67.61:8545/');
    const web3 = new Web3(provider);
    console.log('No web3 instance injected, using preset url.');
    window.web3 = web3;
    return App.init(provider);
  }
});

